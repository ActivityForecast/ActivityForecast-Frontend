function getFallbackByCategory(categoryName) {
  if (categoryName === '유산소') return 걷기;
  if (categoryName === '구기스포츠') return 축구;
    if (categoryName === '익스트림스포츠') return 번지점프;
  if (categoryName === '피트니스') return 헬스;
  return 걷기;
}

export function getActivityImage(activityName, categoryName) {
  if (activityImageMap[activityName]) {
    return activityImageMap[activityName];
  }
  return getFallbackByCategory(categoryName);
}
