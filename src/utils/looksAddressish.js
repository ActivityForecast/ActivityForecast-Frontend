// 지도 위의 위치인지 판별하는 식

export function looksAddressish(q) {
  if (!q) return false;
  const s = q.trim();

  // 주소/지명 패턴 포함 (시, 구, 로, 길, 번지, 역 등)
  if (/[시군구도광역특별자치동읍면리로길대로번지역\s\d-]/.test(s)) {
    return true;
  }

  const known = [
    // 광역단위
    '서울','부산','대구','인천','광주','대전','울산','세종',
    '경기','강원','충북','충남','전북','전남','경북','경남','제주',
    // 서울 주요 구 이름
    '강남','서초','송파','강동','광진','성동','중구','용산','동대문',
    '성북','은평','마포','서대문','영등포','동작','관악','강서','양천',
    '구로','금천','노원','도봉','중랑'
  ];
  if (known.some((k) => s.startsWith(k))) {
    return true;
  }

  // 한글 2자 이상 → 위치로 추정
  if (/^[가-힣]{2,}$/.test(s)) {
    return true;
  }

  return false;
}